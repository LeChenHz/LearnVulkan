    struct decimation_table
    {
        int num_weights;
        int texel_num_weights_[MAX_TEXELS_PER_BLOCK];	// number of indices that go into the calculation for a texel
        int texel_weights_int_[MAX_TEXELS_PER_BLOCK][4];	// the weight to assign to each weight
        float texel_weights_float[MAX_TEXELS_PER_BLOCK][4];	// the weight to assign to each weight
        int texel_weights_[MAX_TEXELS_PER_BLOCK][4];	// the weights that go into a texel calculation
        int weight_texel_[MAX_WEIGHTS_PER_BLOCK][MAX_TEXELS_PER_BLOCK];	// the texels that the weight contributes to
        int weights_int_[MAX_WEIGHTS_PER_BLOCK][MAX_TEXELS_PER_BLOCK];	// the weights that the weight contributes to a texel.
    };

    struct block_mode
    {
        int decimation_mode_;
        int quantization_mode_;
        int is_dual_plane_;
        int permit_decode_;
    };

    struct block_size_descriptor
    {
        decimation_table decimation_tables[MAX_DECIMATION_MODES + 1];
        block_mode block_modes[MAX_WEIGHT_MODES];
    };


    struct partition_info
    {
        int partition_count_;
        int texels_per_partition_[4];
        int partition_of_texel_[MAX_TEXELS_PER_BLOCK];
        int texels_of_partition_[4][MAX_TEXELS_PER_BLOCK];
    };
    struct partition_table {
        partition_info one;
        partition_info two[1024];
        partition_info three[1024];
        partition_info four[1024];
    };

    std::shared_ptr<block_size_descriptor> create_block_size_descriptor(int xdim, int ydim) {
        const angle::astc::block_size_descriptor* src = angle::astc::get_block_size_descriptor(xdim, ydim);
        auto dst = std::make_shared<block_size_descriptor>();

        for (size_t i = 0; i < sizeof(dst->decimation_tables) / sizeof(dst->decimation_tables[0]); ++i) {
            decimation_table* d = dst->decimation_tables + i;
            const angle::astc::decimation_table* const s = src->decimation_tables[i];
            if (!s) {
                continue;
            }

            d->num_weights = s->num_weights;

            for (size_t j = 0; j < sizeof(d->texel_num_weights_) / sizeof(d->texel_num_weights_[0]); ++j) {
                d->texel_num_weights_[j] = s->texel_num_weights[j];
            }

            for (size_t j = 0; j < sizeof(d->texel_weights_int_) / sizeof(d->texel_weights_int_[0]); ++j) {
                for (int k = 0; k < sizeof(d->texel_weights_int_[0]) / sizeof(d->texel_weights_int_[0][0]); ++k) {
                    d->texel_weights_int_[j][k] = s->texel_weights_int[j][k];
                }
            }

            for (size_t j = 0; j < sizeof(d->texel_weights_float) / sizeof(d->texel_weights_float[0]); ++j) {
                for (int k = 0; k < sizeof(d->texel_weights_float[0]) / sizeof(d->texel_weights_float[0][0]); ++k) {
                    d->texel_weights_float[j][k] = s->texel_weights_float[j][k];
                }
            }

            for (size_t j = 0; j < sizeof(d->texel_weights_) / sizeof(d->texel_weights_[0]); ++j) {
                for (int k = 0; k < sizeof(d->texel_weights_[0]) / sizeof(d->texel_weights_[0][0]); ++k) {
                    d->texel_weights_[j][k] = s->texel_weights[j][k];
                }
            }

            for (size_t j = 0; j < sizeof(d->weight_texel_) / sizeof(d->weight_texel_[0]); ++j) {
                for (int k = 0; k < sizeof(d->weight_texel_[0]) / sizeof(d->weight_texel_[0][0]); ++k) {
                    d->weight_texel_[j][k] = s->weight_texel[j][k];
                }
            }

            for (size_t j = 0; j < sizeof(d->weights_int_) / sizeof(d->weights_int_[0]); ++j) {
                for (int k = 0; k < sizeof(d->weights_int_[0]) / sizeof(d->weights_int_[0][0]); ++k) {
                    d->weights_int_[j][k] = s->weights_int[j][k];
                }
            }
        }

        for (size_t i = 0; i < sizeof(dst->block_modes) / sizeof(dst->block_modes[0]); ++i) {
            dst->block_modes[i].decimation_mode_ = src->block_modes[i].decimation_mode;
            dst->block_modes[i].quantization_mode_ = src->block_modes[i].quantization_mode;
            dst->block_modes[i].is_dual_plane_ = src->block_modes[i].is_dual_plane;
            dst->block_modes[i].permit_decode_ = src->block_modes[i].permit_decode;
        }

        return dst;
    }

    void copy_partition_info(partition_info* dst, const angle::astc::partition_info* src) {
        dst->partition_count_ = src->partition_count;

        for (int i = 0; i <
            sizeof(dst->partition_of_texel_) / sizeof(dst->partition_of_texel_[0]);
            ++i) {
            dst->partition_of_texel_[i] = src->partition_of_texel[i];
        }

        for (int i = 0; i <
            sizeof(dst->texels_per_partition_) / sizeof(dst->texels_per_partition_[0]);
            ++i) {
            dst->texels_per_partition_[i] = src->texels_per_partition[i];
        }

        for (int i = 0; i <
            sizeof(dst->texels_of_partition_) / sizeof(dst->texels_of_partition_[0]);
            ++i) {
            for (int j = 0; j < sizeof(dst->texels_of_partition_[0]) / sizeof(dst->texels_of_partition_[0][0]); ++j) {

                dst->texels_of_partition_[i][j] = src->texels_of_partition[i][j];
            }
        }
    }

    std::shared_ptr<partition_table> create_partition_table(int xdim, int ydim) {
        auto pt = std::make_shared<partition_table>();
        copy_partition_info(&pt->one, angle::astc::get_partition_table(xdim, ydim, 1));

        for (int i = 0; i < 1024; ++i) {
            copy_partition_info(pt->two + i, angle::astc::get_partition_table(xdim, ydim, 2) + i);
            copy_partition_info(pt->three + i, angle::astc::get_partition_table(xdim, ydim, 3) + i);
            copy_partition_info(pt->four + i, angle::astc::get_partition_table(xdim, ydim, 4) + i);
        }

        return pt;
    }
